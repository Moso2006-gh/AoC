for structure in raw_data.split("\n\n"):
    is_lock = structure[0] == "#"
    char = "." if is_lock else "#"
    schematic = []
    
    structure = structure.split("\n")
    for x in range(len(structure[0])):
        for y in reversed(range(len(structure))):
            if structure[y][x] == char:
                continue
            schematic.append(y + 1)
            break
    (locks if is_lock else keys).append(schematic)

    print(structure)
    print(schematic)
    print()

