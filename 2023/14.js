let mirror = input.split("\n").map(x => x.split(""));

let pastMirrors = [];

const pushRocks = (moveDir) => {
    for (let i = 0; i < mirror.length; i++) {
        const iReal = moveDir[0] > 0 ? mirror.length - (i + 1) : i;
        for (let j = 0; j < mirror[iReal].length; j++) {
            const jReal = moveDir[1] > 0 ? mirror.length - (j + 1) : j;

            if(mirror[iReal][jReal] !== "O") continue;
            
            let move = 0;
            while(mirror[iReal + moveDir[0]*(move + 1)] !== undefined && mirror[iReal + moveDir[0]*(move + 1)][jReal + moveDir[1]*(move + 1)] !== undefined) {
                if(mirror[iReal + moveDir[0]*(move + 1)][jReal + moveDir[1]*(move + 1)] !== ".") break;
                move++;
            }
            
            mirror[iReal][jReal] = ".";
            mirror[iReal + moveDir[0]*move][jReal + moveDir[1]*move] = "O";
        }
        
    } 
}

while (true) {
    for (let c = 0; c < 4; c++) {

        let moveDir = [];
        switch(c) {
            case 0:
                moveDir = [-1, 0];
                break;
            case 1:
                moveDir = [0, -1];
                break;
            case 2:
                moveDir = [1, 0];
                break;
            case 3:
                moveDir = [0, 1];
                break;
        }

        pushRocks(moveDir);
    }

    if ( pastMirrors.indexOf(JSON.stringify(mirror)) !== -1 ) {
        const length = pastMirrors.length;
        const loopStart = pastMirrors.indexOf(JSON.stringify(mirror));

        const loopLength = length - loopStart;
        const remainingCicles = 1000000000 - (loopStart + 1); 
        const index = remainingCicles % loopLength;

        mirror = JSON.parse(pastMirrors[loopStart + index]);

        let sol = 0;
        mirror.forEach((row, i) => 
            sol += (row.join("").split("O").length - 1) * (mirror.length - i)
        );

        console.log(sol);
        break;
    }

    pastMirrors.push(JSON.stringify(mirror))
}